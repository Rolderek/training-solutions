package javabeans;

public class Human {

    private String name;
    private float weight;
    private int iq;

    public String getName() {
        return name;
    }

    public float getWeight() {
        return weight;
    }

    public int getIq() {
        return iq;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }

    public void setIq(int iq) {
        this.iq = iq;
    }
}
